package it.gpedulla.hellomvc.model;

public class Studente {

	private Long id;
	
	private String name;
	
	public Studente() {}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
